package com.acabes.assignments.java.cohort;

import java.util.Date;

class Cohort4 {
    String name;
    int age;
    String city;
    Date dateOfBirth;

    public Cohort4(String name, int age, String city, Date dateOfBirth) {
        this.name = name;
        this.age = age;
        this.city = city;
        this.dateOfBirth = dateOfBirth;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

}
